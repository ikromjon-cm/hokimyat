export function t(key: string, locale: string = "uz"): string {
  const lang = translations[locale] || translations["uz"];
  return lang[key] || translations["uz"][key] || key;
}

/** Reactive translator: re-renders when the user's language preference changes. */
export function useT() {
  const locale = useAuthStore((s) => s.user?.languagePreference) || "uz";
  return (key: string) => t(key, locale);
}
